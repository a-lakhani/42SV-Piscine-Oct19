int		ft_strcmp(char *s1, char *s2)
{
	char	c1;
	char	c2;
	int		i;

	i = 0;
	c1 = s1[i];
	c2 = s2[i];
	while (c1 == c2)
	{
		i++;
		c1 = s1[i];
		c2 = s2[i];
	}
	return (c1 - c2);
}
