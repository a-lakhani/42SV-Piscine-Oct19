void	ft_print_comb(void)
{
		int i;
		int j;
		int k;

		i = 0;

		while(i < 10)
		{
			j = i + 1;

			while(j < 10)
			{
				k = j + 1;

				while(k < 10)
				{
					ft_putchar(i + 48);
					ft_putchar(j + 48);
					ft_putchar(k + 48);
					
					if(i < 7)
					{
					ft_putchar(',');
					ft_putchar(' ');
					};

					k++;
				};

				j++;
			};

			i++;
		};

};

